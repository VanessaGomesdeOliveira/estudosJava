package exercicios;

//Escreva um programa que exiba a sequência de números de 100 a 200;
//Exemplo: 100 101 ....198 199 200

public class Exercicio01 {
    public static void main(String[] args) {            
            int cont ;
            cont = 100;

            while (cont <= 200) {
                System.out.print(cont + " ");
                cont++;
            }
    }
}
